<h2 class="pagetitle">Frames</h2>
<p>Lafuma uses materials and mechanisms that are simple and safe in all its furniture. Tubular steel and aluminum frames are chosen throughout the range due to their strength, versatility and modern look.</p>

<h2>HLE STEEL TUBES</h2>
<p>With an unbeatable strength to lightness ratio, High Level Elastic (HLE) tubular steel is the only choice for Lafuma's range of steel furniture. HLE steel is 15% lighter than other steels of the same strength but allows a good degree of flexibility. The sheer quality of the HLE steel allows Lafuma to create strong, smooth curves in the frames. HLE steel is not the cheapest but is ideal for use in furniture. The steel frames are painted externally for protection against the elements. This does not, however, eliminate the possibility of rust occurring, so steel frames should not be allowed to get wet.</p>
<h2>ALUMINIUM TUBES</h2>
<p>The aluminum alloys used in our products have been carefully selected based on strict performance criteria. 5049, 5449 or 5086 aluminum is used depending on the product type. Alloy frames are lightweight, easy to handle and not susceptible to age or weather induced wear. For these reasons (and unlike cheaper alternatives), Lafuma's aluminum framed products have been shown to stand the test of time.</p>